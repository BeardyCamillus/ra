import Toolbar from './Toolbar';
import ProjectList from './ProjectList';
import { useState } from 'react'

export default function Portfolio() {

    const initialState = {
        filters: ["All", "Websites", "Flayers", "Business Cards"],
        selected: "All"
    };
    const [state, setState] = useState(initialState);

    const selectFilterHandler = (filter) => {
        setState({
            ...initialState,
            selected: filter.target.innerText
        })
    }

    const filteredProjectList = () => {
         return projectList.filter(project => {
            if (project.category === state.selected) {
                return true
            }

        })
    }

    return (
        <div>
            <div>
                <Toolbar
                    filters={state.filters}
                    selected={state.selected}
                    onSelectFilter={(filter) => selectFilterHandler(filter)}
                />
            </div>
            <div>
                <ProjectList projects={state.selected === "All" ? projectList : filteredProjectList()} />
            </div>
        </div>
    )
}

